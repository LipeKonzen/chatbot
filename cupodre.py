def simnao(msg = '(\033[1;32mS\033[m/\033[1;31mN\033[m)\n\n>> ', msgerro = '\033[1;31mERRO:\033[m digite apenas \033[1;32mS\033[m/\033[1;31mN\033[m'):
    while True:
        escolha = input(msg).upper()
        if len(escolha) == 1 and escolha in 'SN':
            break
        print(msgerro)
    return escolha == 'S'
        


def verificar(msg):
    while True:
        x = input(msg)
        if x.isnumeric():
            x = int(x)
            break
        print('erro: digite apenas números')
    return(x)


