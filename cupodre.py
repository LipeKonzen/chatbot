import os

def titulo(m = 'Título', mcor = '', mnegrito = '', d = '', dcor = '', dnegrito = ''):
    dtamanho = len(m)
    if d != '':
        print(f'\n\033[{dnegrito};{dcor}m', d * dtamanho, '\033[m\n')
    print(f'\033[{mnegrito};{mcor}m', m, '\033[m\n')
    if d != '':
        print(f'\033[{dnegrito};{dcor}m', d * dtamanho, '\033[m\n')

